  function registerExtraScenariosOnce() {
    if (window.__lightExtraScenariosRegistered) return;
    window.__lightExtraScenariosRegistered = true;

    try {
      Object.keys(extraScenarios).forEach(function (key) {
        const item = extraScenarios[key];
        if (typeof badgeState !== 'undefined' && !(key in badgeState)) badgeState[key] = false;
        if (typeof badgeMeta !== 'undefined') badgeMeta[key] = item.badge;
        if (typeof asdGames !== 'undefined') asdGames[key] = item.game;
      });

      addScenarioCardsOnce();
      updateTotalText();
      if (typeof renderBadges === 'function') renderBadges();
    } catch (error) {
      console.warn('extra-scenarios-light failed:', error);
    }
  }

  function addScenarioCardsOnce() {
    const grid = document.querySelector('.scenario-select-grid');
    if (!grid) return;

    Object.keys(extraScenarios).forEach(function (key) {
      if (document.getElementById('scenarioCard-' + key)) return;
      const card = extraScenarios[key].card;
      const html = '<div class="scenario-card" id="scenarioCard-' + escapeHtml(key) + '">' +
        '<div class="emoji">' + escapeHtml(card.icon) + '</div>' +
        '<strong>' + escapeHtml(card.title) + '</strong>' +
        '<div class="small">' + escapeHtml(card.desc) + '</div>' +
        '<button onclick="startAsdGame(\'' + escapeHtml(key) + '\')">開始這個情境</button>' +
        '</div>';
      grid.insertAdjacentHTML('beforeend', html);
    });
  }

  function updateTotalText() {
    const status = document.getElementById('gameStatusText');
    if (status) status.textContent = status.textContent.replace(/\/\s*8/g, '/ ' + TOTAL_SCENARIOS).replace(/\/\s*13/g, '/ ' + TOTAL_SCENARIOS);
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', registerExtraScenariosOnce);
  } else {
    registerExtraScenariosOnce();
  }
